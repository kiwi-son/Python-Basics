# For multiline comments in Python, you can use triple quotes (either ''' or """). Here's an example:
"""I'm Jeet 
My age is 22
"""
#Escape Sequences
# \n is used to add a new line in the string
print("Hello\nWorld")
# \t is used to add a tab space in the string 
print("Hello\tWorld")
# \\ is used to add a backslash in the string
print("This is a backslash: \\")
# \' is used to add a single quote in the string
print('It\'s a beautiful day!')
# \" is used to add a double quote in the string
print("She said, \"Hello!\"")

print('Hello world', "Jeet", 21, sep='---') #sep is used to add a separator between different values
print('Hello world', "Jeet", 21, end=' END\n') #end is used to add a string at the end of the print statement
#here is another example of end
print("jeet sadhukhan", end='.')
print("21")