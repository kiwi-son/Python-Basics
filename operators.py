#Arithmetic Operators Module
a=220
b=15
print("a + b =",a+b) #Addition
print("a - b =",a-b) #Subtraction
print("a * b =",a*b) #Multiplication
print("a / b =",a/b) #Division
print("a // b =",a//b) #Floor Division
print("a % b =",a%b) #Modulus

print("a ** b =",a**b) #Exponentiation

#Comparison Operators Module
print(10>6) #greater than
print(10<6) #less than
print(10==6) #equal
print(10!=6) #not equal
print(10>=6) #greater than or equal to
print(10<=6) #less than or equal to

#Logical Operators Module
x = True
y = False
print(x and y) #logical AND
print(x or y)  #logical OR  
print(not x)    #logical NOT

#assignment Operators Module
c = 10
c += 5
print("c after += 5:", c)
c -= 3
print("c after -= 3:", c)
